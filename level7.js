// The variable 'number' holds the value 10
let number = 10;

// Here we make an if statement: if 'number' is greater than 5,
// it will print "Number is greater than 5"
if (number > 5) {
    document.getElementById("message").innerHTML = "Number is greater than 5";
    
    // Otherwise, it will print "Number is 5 or less"
} else {
    document.getElementById("message").innerHTML = "Number is 5 or less";
}